function operate(operator, number1, number2) {
    if(operator == '+') {
        return number1 + number2;
    }
    if(operator == '-') {
        return number1 - number2;
    }
    if(operator == '*') {
        return number1 * number2;
    }
    if(operator == '/') {
        return number1 / number2;
    }
}